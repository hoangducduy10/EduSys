    void init(){
        setIconImage(ImageHelper.getAppIcon());
        setLocationRelativeTo(null);
        setTitle("QUẢN LÝ NHÂN VIÊN");
        fillTable();
        updateStatus();
    }

    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getMatKhau(),
                    nv.getHoTen(),
                    nv.isVaiTro() ? "Trưởng phòng":"Nhân viên"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void edit(){
        try {
            String maNV = (String)tblNhanVien.getValueAt(this.row, 0);
            NhanVien nv = dao.selectById(maNV);
            if(nv!=null){
                setForm(nv);
                updateStatus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void setForm(NhanVien nv){
        txtMaNV.setText(nv.getMaNV());
        txtHoTenNV.setText(nv.getHoTen());
        txtMatKhau.setText(nv.getMatKhau());
        txtXacNhanMK.setText(nv.getMatKhau());
        rdoTruongPhong.setSelected(nv.isVaiTro());
        rdoNhanVien.setSelected(!nv.isVaiTro());
    }
    
    NhanVien getForm(){
        NhanVien model = new NhanVien();
        model.setMaNV(txtMaNV.getText());
        model.setHoTen(txtHoTenNV.getText());
        model.setMatKhau(new String(txtMatKhau.getPassword()));
        model.setVaiTro(rdoTruongPhong.isSelected());
        return model;
    }
    
    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    
    void clearForm(){
        this.setForm(new NhanVien());
        this.updateStatus();
        row = -1;
        updateStatus();
    }
    
    void insert(){
        NhanVien nv = getForm();
        String confirm = new String(txtXacNhanMK.getPassword());
        if(confirm.equals(nv.getMatKhau())){
            try {
                dao.insert(nv);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại!");
            }
        }else{
            MsgBox.alert(this, "Xác nhận sai mật khẩu!");
        }
    }
    
    void update(){
        NhanVien nv = getForm();
        String confirm = new String(txtXacNhanMK.getPassword());
        if(confirm.equals(nv.getMatKhau())){
            try {
                dao.update(nv);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Cập nhập thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhập thất bại!");
            }
        }else{
            MsgBox.alert(this, "Xác nhận sai mật khẩu!");
        }
    }
    
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa nhân viên!");
        }else{
            if(MsgBox.confirm(this, "Bạn có chắc chắn muốn xóa nhân viên này?")){
                String maNV = txtMaNV.getText();
                try {
                    dao.delete(maNV);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }
    
    void first(){
        row=0;
        edit();
    }
    
    void prev(){
        if(row>0){
            row--;
            edit();
        }
    }
    
    void next(){
        if(row<tblNhanVien.getRowCount()-1){
            row++;
            edit();
        }
    }
    
    void last(){
        row = tblNhanVien.getRowCount()-1;
        edit();
    }
    
