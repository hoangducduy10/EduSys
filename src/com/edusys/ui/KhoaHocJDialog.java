    void init(){
        setLocationRelativeTo(null);
        setTitle("QUẢN LÝ KHÓA HỌC");
        fillCbo();
        fillTable();
        updateStatus();
    }
    
    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblKhoaHoc.getModel();
        model.setRowCount(0);
        try {
            ChuyenDe cd = (ChuyenDe) cboChuyenDe.getSelectedItem();
            List<KhoaHoc> list = khDAO.selectByChuyenDe(cd.getMaCD());
            for (KhoaHoc kh : list) {
                Object[] row = {
                    kh.getMaKH(),
                    kh.getThoiLuong(),
                    kh.getHocPhi(),
                    DateHelper.toString(kh.getNgayKG(), "dd-MM-yyyy"),
                    kh.getMaNV(),
                    DateHelper.toString(kh.getNgayTao(), "dd-MM-yyyy")
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void fillCbo(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboChuyenDe.getModel();
        model.removeAllElements();
        List<ChuyenDe> list = cdDAO.selectAll();
        for (ChuyenDe cd : list) {
            model.addElement(cd);
        }
    }
    
    void chonCD(){
        ChuyenDe cd = (ChuyenDe) cboChuyenDe.getSelectedItem();
        txtThoiLuong.setText(String.valueOf(cd.getThoiLuong()));
        txtHocPhi.setText(String.valueOf(cd.getHocPhi()));
        txtGhiChu.setText(cd.getMoTa());
        lblChuyenDe.setText(cd.getTenCD());
        fillTable();
        row=-1;
    }
    
    void setForm(KhoaHoc kh){
        cboChuyenDe.setSelectedItem(cdDAO.selectById(kh.getMaCD()));
        cboChuyenDe.setToolTipText(String.valueOf(kh.getMaKH()));
        txtKhaiGiang.setText(DateHelper.toString(kh.getNgayKG(), "dd-MM-yyyy"));
        txtNgayTao.setText(DateHelper.toString(kh.getNgayKG(), "dd-MM-yyyy"));
        txtNguoiTao.setText(kh.getMaNV());
        txtHocPhi.setText(String.valueOf(kh.getHocPhi()));
        txtThoiLuong.setText(String.valueOf(kh.getThoiLuong()));
        txtGhiChu.setText(kh.getGhiChu());
    }
    
    KhoaHoc getForm(){
        KhoaHoc kh = new KhoaHoc();
        ChuyenDe cd = (ChuyenDe) cboChuyenDe.getSelectedItem();
        if(txtNguoiTao.getText().equals("")){
            kh.setMaNV(Auth.user.getMaNV());
        }else{
            kh.setMaNV(txtNguoiTao.getText());
        }
        kh.setMaCD(cd.getMaCD());
        kh.setThoiLuong(Integer.parseInt(txtThoiLuong.getText()));
        kh.setNgayKG(DateHelper.toDate(txtKhaiGiang.getText(), "dd-MM-yyyy"));
        kh.setHocPhi(Double.parseDouble(txtHocPhi.getText()));
        kh.setGhiChu(txtGhiChu.getText());
        kh.setMaKH(Integer.parseInt(cboChuyenDe.getToolTipText()));
        
        return kh;
    }
    
    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblKhoaHoc.getRowCount() - 1);
        txtHocPhi.setEditable(!edit);
        txtThoiLuong.setEditable(!edit);
        txtNguoiTao.setEditable(!edit);
        txtNgayTao.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    
    void edit(){
        try {
            int maKH = (int)tblKhoaHoc.getValueAt(row, 0);
            KhoaHoc kh = khDAO.selectById(maKH);
            if(kh!=null){
                setForm(kh);
                tabs.setSelectedIndex(0);
                updateStatus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void insert(){
        KhoaHoc kh = getForm();
        try {
            khDAO.insert(kh);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
            e.printStackTrace();
        }
    }
    
    void update(){
        KhoaHoc kh = getForm();
        try {
            khDAO.update(kh);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Cập nhập thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhập thất bại!");
            e.printStackTrace();
        }
    }
    
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa khóa học này!");
        }else{
            int id = Integer.parseInt(cboChuyenDe.getToolTipText());
            if(MsgBox.confirm(this, "Bạn có chắc chắn muốn xóa khóa học này?")){
                try {
                    khDAO.delete(id);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                    e.printStackTrace();
                }
            }
        }
    }
    
    void clearForm(){
        this.setForm(new KhoaHoc());
        row = -1;
        updateStatus();
    }
    
    void first(){
        row=0;
        edit();
    }
    
    void prev(){
        if(row>0){
            row--;
            edit();
        }
    }
    
    void next(){
        if(row<tblKhoaHoc.getRowCount()-1){
            row++;
            edit();
        }
    }
    
    void last(){
        row = tblKhoaHoc.getRowCount()-1;
        edit();
    }

