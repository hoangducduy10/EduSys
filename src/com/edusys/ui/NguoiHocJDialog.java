    void init(){
        setLocationRelativeTo(null);
        setTitle("QUẢN LÝ NGƯỜI HỌC");
        fillTable();
        updateStatus();
    }
    
    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblNguoiHoc.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtTimKiem.getText();
            List<NguoiHoc> list = dao.selectByKeyWord(keyword);
            for (NguoiHoc nh : list) {
                Object[] rows = {
                    nh.getMaNH(),
                    nh.getHoTen(),
                    nh.isGioiTinh() ? "Male":"Female",
                    DateHelper.toString(nh.getNgaySinh(), "dd-MM-yyyy"),
                    nh.getDienThoai(),
                    nh.getEmail(),
                    nh.getMaNV(),
                    nh.getNgayDK()
                };
                model.addRow(rows);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void setForm(NguoiHoc nh){
        txtMaNH.setText(nh.getMaNH());
        txtHoTen.setText(nh.getHoTen());
        txtNgaySinh.setText(DateHelper.toString(nh.getNgaySinh(), "dd-MM-yyyy"));
        txtEmail.setText(nh.getEmail());
        txtDienThoai.setText(nh.getDienThoai());
        txtGhiChu.setText(nh.getGhiChu());
        rdoNam.setSelected(nh.isGioiTinh());
        rdoNu.setSelected(!nh.isGioiTinh());
    }
    
    NguoiHoc getForm(){
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNH(txtMaNH.getText());
        nh.setHoTen(txtHoTen.getText());
        nh.setDienThoai(txtDienThoai.getText());
        nh.setEmail(txtEmail.getText());
        nh.setGhiChu(txtGhiChu.getText());
        nh.setNgaySinh(DateHelper.toDate(txtNgaySinh.getText(), "dd-MM-yyyy"));
        nh.setNgayDK(new Date());
        nh.setGioiTinh(rdoNu.isSelected());
        nh.setMaNV(Auth.user.getMaNV());
        
        return nh;
    }
    
    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNguoiHoc.getRowCount() - 1);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    
    void edit(){
        try {
            String maNH = (String)tblNguoiHoc.getValueAt(row, 0);
            NguoiHoc nh = dao.selectById(maNH);
            if(nh!=null){
                setForm(nh);
                tabs.setSelectedIndex(0);
                this.updateStatus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void insert(){
        NguoiHoc nh = getForm();
        try {
            dao.insert(nh);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
            e.printStackTrace();
        }
    }
    
    void update(){
        NguoiHoc nh = getForm();
        try {
            dao.update(nh);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Cập nhập thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhập thất bại!");
            e.printStackTrace();
        }
    }
    
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa người học này!");
        }else{
            String maNH = txtMaNH.getText();
            if(MsgBox.confirm(this, "Bạn có chắc chắn muốn xóa khóa học này?")){
                try {
                    dao.delete(maNH);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                    e.printStackTrace();
                }
            }
        }
    }
    
    void clearForm(){
        this.setForm(new NguoiHoc());
        this.updateStatus();
        row = -1;
        updateStatus();
    }
    
    void timKiem() {
        this.fillTable();
        this.clearForm();
        this.row = -1;
        this.updateStatus();
    }
    
    void first(){
        row=0;
        edit();
    }
    
    void prev(){
        if(row>0){
            row--;
            edit();
        }
    }
    
    void next(){
        if(row<tblNguoiHoc.getRowCount()-1){
            row++;
            edit();
        }
    }
    
    void last(){
        row = tblNguoiHoc.getRowCount()-1;
        edit();
    }

